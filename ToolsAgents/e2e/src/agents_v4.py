## Use LLM-as-a-Judge Evaluator

# -----------------------------------------
# ENV
# -----------------------------------------
load_dotenv()

VECTOR_DB_PATH = "hr_faiss_index"

# -----------------------------------------
# LOAD VECTOR DB
# -----------------------------------------
embeddings = OpenAIEmbeddings(model="text-embedding-3-small")

vectorstore = FAISS.load_local(
    VECTOR_DB_PATH,
    embeddings,
    allow_dangerous_deserialization=True
)

retriever = vectorstore.as_retriever(search_kwargs={"k": 4})

# -----------------------------------------
# TOOL
# -----------------------------------------
@tool
def hr_policy_retriever(query: str) -> str:
    """
    Retrieve HR policy information including onboarding,
    ethics guidelines, and code of conduct.
    """

    docs = retriever.invoke(query)

    results = []

    for doc in docs:
        source = doc.metadata.get("source", "Unknown")
        page = doc.metadata.get("page", "N/A")

        results.append(
            f"Source: {source} | Page: {page}\n{doc.page_content}"
        )

    return "\n\n".join(results)

@tool
def ethics_violation_detector(query: str) -> str:
    """
    Detect whether a user query involves unethical, illegal,
    or policy-violating behavior in the workplace.
    """

    prompt = f"""
    You are an AI ethics compliance reviewer.

    Determine whether the following employee request violates
    workplace ethics or company policy.

    Respond with ONLY one of the following:

    SAFE
    ETHICS_VIOLATION

    Query:
    {query}
    """

    response = llm.invoke(prompt)

    return response.content

## Added new tool 
@tool
def response_evaluator(data: str) -> str:
    """
    Evaluate whether the generated response is correct,
    ethical, and grounded in company HR policies.
    """

    prompt = f"""
    You are an AI system responsible for evaluating responses
    generated by an HR assistant.

    Evaluate the response using these criteria:

    1. Relevance to the user question
    2. Alignment with HR policies
    3. Ethical and professional tone

    Return output strictly in this format:

    Verdict: PASS or FAIL
    Reason: Short explanation

    Input:
    {data}
    """

    response = llm.invoke(prompt)

    return response.content


