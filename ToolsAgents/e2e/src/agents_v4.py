# -----------------------------------------
# LLM
# -----------------------------------------
llm = ChatOpenAI(
    model="gpt-4o-mini",
    temperature=0
)



tools = [
    hr_policy_retriever,
    ethics_violation_detector,
    response_evaluator
]


# -----------------------------------------
# PROMPT
# -----------------------------------------
from langchain.prompts import PromptTemplate

# -----------------------------------------
# **UPDATED** CUSTOM PROMPT
# -----------------------------------------
template = """

    You are an HR Ethics and Compliance Assistant.

    Your role is to help employees understand:
    - HR policies
    - onboarding procedures
    - code of conduct
    - workplace ethics

    You must answer questions using company policy documents.

    Workflow you must follow:

    1. If the question may involve unethical behavior,
    first use the ethics_violation_detector tool.

    2. If the query is SAFE, retrieve relevant policy using hr_policy_retriever.

    3. Generate the answer using retrieved information.

    4. Before giving the final answer, ALWAYS evaluate the answer using
    the response_evaluator tool.

    If the evaluator returns FAIL, revise the answer.

    You have access to the following tools:

    {tools}

    You may use the following tool names:

    {tool_names}

    Use the following format:

    Question: the employee question

    Thought: think about what to do

    Action: one of [{tool_names}]

    Action Input: the input to the tool

    Observation: the result returned by the tool

    (repeat if necessary)

    Thought: I now know the final answer

    Final Answer: provide the final answer to the employee.

    Question: {input}

    {agent_scratchpad}

"""
prompt = PromptTemplate.from_template(template)


# -----------------------------------------
# CREATE AGENT
# -----------------------------------------
agent = create_react_agent(
    llm,
    tools,
    prompt
)

# agent_executor = AgentExecutor(
#     agent=agent,
#     tools=tools,
#     verbose=True
# )

agent_executor = AgentExecutor(
    agent=agent,
    tools=tools,
    verbose=True,
    handle_parsing_errors=True
)

# -----------------------------------------
# USER LOOP
# -----------------------------------------
while True:

    query = input("\nAsk HR Assistant: ")

    if query.lower() in ["exit", "quit"]:
        break

    response = agent_executor.invoke({"input": query})

    print("\nAnswer:\n")
    print(response["output"])

